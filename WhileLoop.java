// Java program to print numbers from 1 to 10 using while loop

import java.io.*;
import java.lang.*;
import java.util.*;


class WhileLoop
{
public static void main(String args[])
{
int i=1; //initilization

while(i<=10) // condition
{
System.out.print(i+"\t"); //\t-(tab) (space)

i=i+1; // increment
}

}
}